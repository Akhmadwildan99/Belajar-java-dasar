public class StringExpleined {
    public static void main(String[] args) {
        String text = "klsklgjfjkjgkfkhihrihryrtwyoytoihvlknvnksdkf";

        System.out.println("The length of text is " + text.length());

        String cases = "Hello World";
        System.out.println(cases.toUpperCase());
        System.out.println(cases.toLowerCase());

        String specialText = "I really \"Love\" You";
        System.out.println(specialText);

        String txt = "it\'s all right";
        System.out.println(txt);

        String blackLash = "This is text which include \\ the blacklash";
        System.out.println(blackLash);
    }
}
